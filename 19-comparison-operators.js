//Comparison Operators - compare values and return true or false (boolean).
//< <= > => == !=
//boolean is either true or false

console.log(10 == 10); //true
console.log(10 != 10); //false
console.log(10 < 10); //false
console.log(10 <= 10); //true
console.log(10 > 10); //false
console.log(10 >= 10); //true

console.log("A" == "B"); //false
console.log("A" != "B"); //true
