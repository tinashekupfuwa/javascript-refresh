//Increment and Decrement Operators

var number = 0;
//Postfix Increment
console.log("Prefix");
console.log(number++); //it will output 0 because
console.log(number--); //it will output 0 because

//Prefix Increment
console.log("Prefix");
var numberTwo = 0;
console.log(++numberTwo);
console.log(--numberTwo);
