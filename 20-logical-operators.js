//Logical Operators - allow us to connect two or more expressions.
// && ||
console.log(10 == 10 && "A" == "A"); // && means everything has to be true.
console.log(10 == 10 && "A" == "B"); //false as A is not equal to B.

console.log(10 == 10 || "A" == "B"); //||(or) is used when one or more expression is true.
